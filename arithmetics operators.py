#almost similar to the operators used in maths!

print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
#and let say in some cases we dont need numbers coming after decimal we only want integer number as answer so we will put this // sign to remove all the number coming after decimal.
print(5 // 2)
#remainder operator:% also known as modulo.when we divide one number with another at that time this operator gives us the remainder left.
print(5 % 2)
print(5 ** 2)